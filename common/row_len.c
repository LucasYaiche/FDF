#include "fdf.h"

size_t	row_len(char *row)
{
	size_t	len;
	int		i;

	i = -1;
	len = -1;
	while (row[++i])
	{
		while (row[i] == ' ' || row[i] == '-' || (row[i] >= '1' && row[i] <= 0))
			i++;
		len++;
	}
	return (len);
}
